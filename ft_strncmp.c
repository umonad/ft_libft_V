#include <stdlib.h>

int ft_strncmp(const char *s1, const char *s2, size_t n)
{
    int i;

    i = 0;
    
    if(n == 0)
        return(0);
    while(n > 0 && s1[i] == s2[i] && s1[n] != '\0' && s2[n] != '\0')
        n--;
    return(s1[n] - s2[n]);
}
